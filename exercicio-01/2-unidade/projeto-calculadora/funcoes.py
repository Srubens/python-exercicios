
#somar
def soma(numero1,numero2):
  resultado = numero1+numero2
  return resultado
  

#subtrair
def subtract(numbA,numbB):
  result = numbA - numbB
  return result


#multiplicar
def mult(a,b):
  result = a * b
  return result

#dividir
def division(num1,num2):
  result=num1/num2
  return result





